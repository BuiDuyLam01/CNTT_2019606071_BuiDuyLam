package model;

public class khoiA06 {
	private String maThiSinh;
	private String hoVaTen;
	private int namSinh;
	private int gioiTinh;
	private float toan;
	private float li;
	private float su;
	private float uuTien;
	private float tongdiem;
	private float diemHocBa;
	
	public khoiA06() {
		
	}

	public khoiA06(String maThiSinh, String hoVaTen, int namSinh, int gioiTinh, float toan, float li, float su,
			float uuTien, float tongdiem, float diemHocBa) {
		super();
		this.maThiSinh = maThiSinh;
		this.hoVaTen = hoVaTen;
		this.namSinh = namSinh;
		this.gioiTinh = gioiTinh;
		this.toan = toan;
		this.li = li;
		this.su = su;
		this.uuTien = uuTien;
		this.tongdiem = tongdiem;
		this.diemHocBa = diemHocBa;
	}

	public String getMaThiSinh() {
		return maThiSinh;
	}

	public void setMaThiSinh(String maThiSinh) {
		this.maThiSinh = maThiSinh;
	}

	public String getHoVaTen() {
		return hoVaTen;
	}

	public void setHoVaTen(String hoVaTen) {
		this.hoVaTen = hoVaTen;
	}

	public int getNamSinh() {
		return namSinh;
	}

	public void setNamSinh(int namSinh) {
		this.namSinh = namSinh;
	}

	public int getGioiTinh() {
		return gioiTinh;
	}

	public void setGioiTinh(int gioiTinh) {
		this.gioiTinh = gioiTinh;
	}

	public float getToan() {
		return toan;
	}

	public void setToan(float toan) {
		this.toan = toan;
	}

	public float getLi() {
		return li;
	}

	public void setLi(float li) {
		this.li = li;
	}

	public float getSu() {
		return su;
	}

	public void setSu(float su) {
		this.su = su;
	}

	public float getUuTien() {
		return uuTien;
	}

	public void setUuTien(float uuTien) {
		this.uuTien = uuTien;
	}

	public float getTongdiem() {
		return tongdiem;
	}

	public void setTongdiem(float tongdiem) {
		this.tongdiem = tongdiem;
	}

	public float getDiemHocBa() {
		return diemHocBa;
	}

	public void setDiemHocBa(float diemHocBa) {
		this.diemHocBa = diemHocBa;
	}

	
	
}
